# Configuration for the Font Size plugin

# Set to True to enable font size override for desktop viewports.
ENABLED_DESKTOP = False

# Set to True to enable font size override for mobile viewports.
ENABLED_MOBILE = True

# Base font size for desktop viewports (in percent).
# 100% means the theme's default size.
DESKTOP_FONT_SIZE_PERCENT = 100
# Base font size for mobile viewports (in percent).
MOBILE_FONT_SIZE_PERCENT = 80

# The viewport width in pixels below which the mobile font size is applied.
MOBILE_BREAKPOINT_PX = 1012
